            <h5 class="header1">What is a Function, Anyways?</h5>

            <p>A function is a reusable block of code that performs a specified task, possibly calculating a value to return.</p>

            <h5 class="header1">Why Even Use Functions?</h5>
            
            <p class="indent">There are many good reasons to use functions.</p>

            <p class="indent">Primarily, we don't want to repeat code unnecessarily.</p>

            <h5 class="header1">Keep it DRY</h5>

            <blockquote>Don't repeat yourself. In software engineering, don't repeat yourself (DRY) is a principle of software development, aimed at reducing repetition of information of all kinds, especially useful in multi-tier architectures.</blockquote>

            <p class="indent">
                We want to look for instances in which we're repeating our code to identify opportunities
                to extrapolate those duplicate lines into a function.  That's how we avoid repetition!
